CREATE TABLE IF NOT EXISTS user (
    id PRIMARY KEY INTEGER,
    avatar_url TEXT,
    country_code TEXT,
    default_group TEXT,
    is_active TEXT,
    is_bot TEXT,
    is_deleted TEXT,
    is_online TEXT,
    is_supporter TEXT,
    last_visit INTEGER,
    pm_friends_only TEXT,
    profile_colour INTEGER,
    username TEXT,
    country_name TEXT,
    cover_custom_url TEXT,
    cover_url TEXT,
    cover_id INTEGER,
    groups TEXT,
    osu_id,
    taiko_id,
    fruits_id,
    mania_id,
    team_id
);

