    count_miss INTEGER,
    level_current INTEGER,
    level_progress INTEGER,
    global_rank INTEGER,
    global_rank_exp INTEGER,
    pp NUMERIC,
    pp_exp INTEGER,
    ranked_score INTEGER,
    hit_accuracy NUMERIC,
    play_count INTEGER,
    play_time INTEGER,
    total_score INTEGER,
    total_hits INTEGER,
    maximum_combo INTEGER,
    replays_watched_by_others INTEGER,
    is_ranked TEXT,
    grade_counts_ss INTEGER,
    grade_counts_ssh INTEGER,
    grade_counts_s INTEGER,
    grade_counts_sh INTEGER,
    grade_counts_a INTEGER
);

